def apply_translations(file_path, all_translations):
    """Застосування перекладів до файлу"""
    try:
        # Читаємо файл
        with open(file_path, 'r', encoding='utf-8-sig') as f:
            lines = f.readlines()

        # Обробляємо рядки 3000-4000
        modified = False
        for i in range(2999, min(4000, len(lines))):
            line = lines[i]

            # Пропускаємо порожні рядки і коментарі
            if not line.strip() or line.strip().startswith('#'):
                continue

            # Шукаємо ключ перекладу
            if ':' in line:
                parts = line.split(':', 1)
                key = parts[0].strip()

                if key in all_translations:
                    # Зберігаємо відступ
                    indent = len(line) - len(line.lstrip())
                    new_line = ' ' * indent + key + ': "' + all_translations[key] + '"\n'
                    lines[i] = new_line
                    modified = True
                    print(f"Переклав: {key}")

        # Записуємо назад, якщо були зміни
        if modified:
            with open(file_path, 'w', encoding='utf-8-sig') as f:
                f.writelines(lines)
            print(f"\n✅ Файл оновлено: {file_path}")
            return True
        else:
            print("\n⚠️ Нічого не змінено")
            return False

    except Exception as e:
        print(f"\n❌ Помилка: {e}")
        return False

