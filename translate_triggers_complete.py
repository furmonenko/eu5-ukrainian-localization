def translate_text(text):
    """Переклад тексту з використанням словника"""
    result = text

    # Сортуємо за довжиною (довші фрази першими)
    sorted_translations = sorted(TRANSLATIONS.items(), key=lambda x: len(x[0]), reverse=True)

    for ru_text, uk_text in sorted_translations:
        # Використовуємо word boundaries для точнішого перекладу
        # Але дозволяємо певні символи після слів
        pattern = r'\b' + re.escape(ru_text) + r'(?=\b|[«»:\.,;\)\]\}\s])'
        result = re.sub(pattern, uk_text, result, flags=re.IGNORECASE)

    return result


def translate_line(line_num, text):
    """Переклад одного рядка з збереженням всіх макросів"""

    # Якщо це не рядок з перекладом (коментар або ключ), повертаємо як є
    if not ':' in text or text.strip().startswith('#'):
        return text

    # Розділяємо на ключ та значення
    parts = text.split(':', 1)
    if len(parts) != 2:
        return text

    key = parts[0]
    value = parts[1]

    # Перекладаємо тільки значення
    translated_value = translate_text(value)

    return key + ':' + translated_value


